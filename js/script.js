var app = new Vue(
  {
    el: '#root',
    // -------------------------------------------------------------------------
    data: {
      dischi: [],
    },
    // -------------------------------------------------------------------------
    mounted: function() {
      axios.get('http://localhost/php-ajax-dischi/api.php')
      .then((risposta) => {
        this.dischi = risposta.data;
      });
    },
    // -------------------------------------------------------------------------
  }
);